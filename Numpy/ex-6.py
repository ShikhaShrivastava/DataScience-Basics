#Mathematical Operation(1-D)

import numpy as np
a= np.array([10,20,30,40,50])
b= np.array([1,2,3,4,5])

add = np.add(a,b)
print(add)
sum= np.sum(a)
print(sum)
print(a+b)
print()

sub= np.subtract(a,b)
print(sub)
print(a-b)
print()

mul= np.multiply(a,b)
print(mul)
print(a*b)

div=np.divide(a,b)
print(div)
print(a/b)
print()

sq=np.sqrt(a)
print(sq)
print()

logg= np.log(b)
print(logg)
